type ProjectLike = { industry?: string; service?: string; tech?: string[] };

// Pick a representative icon for a project based on its industry/service/tech.
export function projectIcon(p: ProjectLike): string {
  const hay = [p.industry, p.service, ...(p.tech ?? [])].join(' ').toLowerCase();
  if (/(e-?commerce|store|shop|retail|apparel|fashion)/.test(hay)) return 'cart';
  if (/(news|media|portal|magazine|press|berita)/.test(hay)) return 'newspaper';
  if (/(seo|search)/.test(hay)) return 'search';
  if (/(ai|saas|software|platform|app|dashboard|system|tool)/.test(hay)) return 'cpu';
  return 'globe';
}
